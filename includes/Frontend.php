<?php

namespace SocialLinker;

/**
 * Класс для фронтенд-части плагина.
 */
class Frontend {

	/**
	 * Настройки плагина.
	 *
	 * @var array
	 */
	private $settings;

	/**
	 * Конструктор класса.
	 */
	public function __construct() {
		// Получаем настройки
		$this->settings = get_option( 'social_linker_settings', [] );

		// Проверяем, включен ли плагин
		if ( ! isset( $this->settings['enabled'] ) || ! $this->settings['enabled'] ) {
			return;
		}

		// Добавляем плавающее окошко в футер
		add_action( 'wp_footer', [ $this, 'render_floating_box' ] );

		// Подключаем стили и скрипты
		add_action( 'wp_enqueue_scripts', [ $this, 'enqueue_assets' ] );
	}

	/**
	 * Подключает стили и скрипты для фронтенда.
	 */
	public function enqueue_assets() {
		// Стили
		wp_enqueue_style(
			'social-linker-frontend',
			SOCIAL_LINKER_PLUGIN_URL . 'assets/styles/frontend.css',
			[],
			SOCIAL_LINKER_VERSION
		);

		// Скрипты
		wp_enqueue_script(
			'social-linker-frontend',
			SOCIAL_LINKER_PLUGIN_URL . 'assets/scripts/frontend.js',
			[ 'jquery' ],
			SOCIAL_LINKER_VERSION,
			true
		);

		// Передаем данные в JavaScript
		wp_localize_script(
			'social-linker-frontend',
			'socialLinkerData',
			[
				'position' => isset( $this->settings['position'] ) ? $this->settings['position'] : 'right',
			]
		);
	}

	/**
	 * Отображает плавающее окошко с социальными ссылками.
	 */
	public function render_floating_box() {
		// Проверяем, есть ли социальные сети
		if ( empty( $this->settings['social_links'] ) ) {
			return;
		}

		// Получаем активные социальные сети
		$active_links = array_filter(
			$this->settings['social_links'],
			function ( $link ) {
				return isset( $link['enabled'] ) && $link['enabled'] && ! empty( $link['url'] );
			}
		);

		// Если нет активных ссылок, ничего не выводим
		if ( empty( $active_links ) ) {
			return;
		}

		// Определяем позицию
		$position = isset( $this->settings['position'] ) ? $this->settings['position'] : 'right';

		// Выводим HTML
		?>
		<div class="social-linker-floating-box social-linker-position-<?php echo esc_attr( $position ); ?>">
			<div class="social-linker-toggle">
				<span class="social-linker-toggle-icon">
					<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24" width="24" height="24">
						<path d="M18 16.08c-.76 0-1.44.3-1.96.77L8.91 12.7c.05-.23.09-.46.09-.7s-.04-.47-.09-.7l7.05-4.11c.54.5 1.25.81 2.04.81 1.66 0 3-1.34 3-3s-1.34-3-3-3-3 1.34-3 3c0 .24.04.47.09.7L8.04 9.81C7.5 9.31 6.79 9 6 9c-1.66 0-3 1.34-3 3s1.34 3 3 3c.79 0 1.5-.31 2.04-.81l7.12 4.16c-.05.21-.08.43-.08.65 0 1.61 1.31 2.92 2.92 2.92s2.92-1.31 2.92-2.92c0-1.61-1.31-2.92-2.92-2.92zM18 4c.55 0 1 .45 1 1s-.45 1-1 1-1-.45-1-1 .45-1 1-1zM6 13c-.55 0-1-.45-1-1s.45-1 1-1 1 .45 1 1-.45 1-1 1zm12 7.02c-.55 0-1-.45-1-1s.45-1 1-1 1 .45 1 1-.45 1-1 1z" />
					</svg>
				</span>
			</div>

			<div class="social-linker-links">
				<?php foreach ( $active_links as $link ) : ?>
					<a href="<?php echo esc_url( $link['url'] ); ?>" target="_blank" rel="noopener noreferrer" class="social-linker-link social-linker-<?php echo esc_attr( $link['id'] ); ?>" title="<?php echo esc_attr( $link['name'] ); ?>">
						<?php echo $this->get_icon( $link ); ?>
						<span class="social-linker-label"><?php echo esc_html( $link['name'] ); ?></span>
					</a>
				<?php endforeach; ?>
			</div>
		</div>
		<?php
	}

